// Variavéis em javascript => São maneiras de armazenarmos informações para usarmos posteriomente 

//VAR => Maneira antiga de variavel dentro do JS, permite add um valor e posteriomente alterar o valor
var nome = "David";
console.log(`Meu nome é: ${nome}`);


//LET => Add um Valor e também podemos alterar o valor 
let nomeLet = "David";
console.log(`Meu nome é: ${nomeLet}`);

//CONST => Add um valor e não podemos mais substituir esse valor
const nomeConst = "David";
console.log(` =Meu nome é: ${nomeConst}`);